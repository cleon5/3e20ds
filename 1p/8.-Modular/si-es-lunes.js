var x=11;
var dia;
switch(x%7){
    case 0 :
        dia="Lunes";
        break;
    case 1 :
        dia="Martes";
        break;
    case 2 :
        dia="Miercoles";
        break;
    case 3 :
        dia="Jueves";
        break;
    case 4 :
        dia="Viernes";
        break;
    case 5 :
        dia="Sabado";
        break;
    case 6 :
        dia="Domingo";
        break;
        
}
console.log("Si hoy es lunes en 11 dias es "+dia);